<h1>Methodology</h1>
<br>
The ranking of Los Angeles TRI facilities was done with the following
variables:
<br>
<br>
<h2>1. Releases (lbs.)</h2>
<h3>Definition:</h3> Amount of Total On- and Off-site Releases as reported in
Form R Section 8, Source Reduction and Recycling Activities/ Pollution
Prevention. Reported from RY 1987 through 2002. Form R Section 8
reporting categories include subcategories: (8.1a) Total on-site
disposal to Class I Underground Injection Weels, RCRA Subtitile C
landfills, and other landfills, (8.1b) Total other on-site disposal or
other releases, (8.1c) Total off-site disposal to Class I Underground
Injection Wells, RCRA Subtitle C landfills, and other landfills, (8.1d)
Total other off-site disposal or other releases<br>

<h3>Database:</h3> TRI

<br>
<br>

<h2>2. Releases per $1000 of Revenue (lbs./$)</h2>
<h3>Definition:</h3> Pounds of total releases per $1000 revenue, for a
particular facility.<br>
<h3>Database:</h3> TRI, ReferenceUSA, Hoovers, Orbis, InsideView<br>
<br>

<h2>3. Percent of Preferred Waste Management Activities (%)</h2>
<h3>Definition:</h3> The amount of toxic releases that are managed using
preferred waste management activities such as energy recovery,
recycling, and treatment (sum of quantities in Form R Section 8.2 -
8.7) as a calculated percentage of the Total Production-related Waste
Managed (sum of quantities in Form R section 8.1 - 8.7).Preferred Waste
Management Activities are based on the EPA's Waste Management Hierarchy.<br>
<h3>Database:</h3> TRI
<br>
<br>
<h2>4. Release Toxicity (lbs * toxicity)</h2>
<h3>Definition:</h3> The percentage share of a facility's toxic releases from
the total toxic releases of all facilities in the same industry.
<h3>Database:</h3> TRI
<br>
<br>
<h2>5. Lifetime Cancer Risk (number of cancers per million persons)</h2>
<h3>Definition:</h3> The cancer risk associated with the toxicity of the
facility's releases across the Los Angeles Basin as a volume and over a
70 year exposure period.<br>
<h3>Database:</h3> TRI, RSEI, OEHHA
<br>
<br>
<h2>6. DEA Score</h2>
<h3>Definition:</h3> Facility efficiency score based on Data Envelopment
Analysis using the software, MaxDEA. The facility efficiency score
determines how efficient a facility is given its inputs and outputs
compared to similar benchmark facilities within the same industry. In
this analysis, efficiency is measured by the facility's annual revenue
profits and preferred waste management practices(4) given its Total
Releases (1a) and Toxicity of Air Emissions (3). A score of 1 means the
facility is at the efficiency frontier (most efficient) and inversely,
a score of 0 means the facility is furthest away from the frontier
(least efficient).<br>
<h3>Database:</h3> MaxDEA Software
